package com.deepu;

public class arithmetic {

	public static void main(String[] args) {
		int num1, num2, ans;
		num1=20;
		num2=15;
		
		//subtraction
		
		ans=num1-num2;
		System.out.println("subtraction result="+ans);
		
		//multiplication
		ans=num1*num2;
		System.out.println("multpication result="+ans);
		
		//division
		ans=num1/num2;
		System.out.println("division result="+ans);	
	}

}
